export function add(a, b) {
    console.log("이것은 더하기!");
    return a + b;
}

export function subtract(a, b) {
    console.log("이것은 빼기!");
    return a - b;
}

export function multiply(a, b) {
    console.log("이것은 곱하기!");
    return a * b;
}

export function divide(a, b) {
    console.log("이것은 나누기!");
    return a / b;
}
